CREATE TABLE BRANCH(
    BNAME VARCHAR(20),
    CITY VARCHAR(20),
    PRIMARY KEY(BNAME)
);

CREATE TABLE CUSTOMERS(
    CNAME VARCHAR(50),
    CITY VARCHAR(30),
    PRIMARY KEY(CNAME)
);
CREATE TABLE DEPOSIT(
    ACTNO NUMBER(5),
    CNAME VARCHAR(50),
    BNAME VARCHAR(20),
    AMOUNT DECIMAL(10,2),
    ADATE DATE,
    PRIMARY KEY(ACTNO),
    FOREIGN KEY(BNAME) REFERENCES BRANCH(BNAME)
);

CREATE TABLE BORROW(
    LOANNO NUMBER(5),
    CNAME VARCHAR(50),
    BNAME VARCHAR(20),
    AMOUNT DECIMAL(10,2),
    PRIMARY KEY(LOANNO),
    FOREIGN KEY(BNAME) REFERENCES BRANCH(BNAME)
);

